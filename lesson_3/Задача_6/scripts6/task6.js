let weekday = parseFloat(prompt('Введіть номер дня тижня','1'))

if (weekday === 1) {
  document.write('Понеділок')
} else if (weekday === 2) {
  document.write('Вівторок')
} else if (weekday === 3) {
  document.write('Середа')
} else if (weekday === 4) {
  document.write('Четвер')
} else if (weekday === 5) {
  document.write('П\'ятниця')
} else if (weekday === 6) {
  document.write('Субота')
} else if (weekday === 7) {
  document.write('Неділя')
}